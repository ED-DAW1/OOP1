package daw.ed.oop1.geo;

/**
 * Rectangle class
 * @author carlos
 */
public class Rectangle {
    private int length;
    private int width;
    
    public Rectangle() {
        this.length = 0;
        this.width = 0;
    }
    
    public Rectangle(int length, int width) {
        this.length = length;
        this.width = width;
    }

}
